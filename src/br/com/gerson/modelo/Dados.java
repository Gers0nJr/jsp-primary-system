package br.com.gerson.modelo;

public class Dados {
	
	private int id;
	private String nome;
	private String usuario;
	private String senha;
	private String celular;
	private String telefone;
	private String email_pessoal;
	private String email_particular;
	private String departamento;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	public String getCelular() {
		return celular;
	}
	public void setCelular(String celular) {
		this.celular = celular;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	public String getEmail_pessoal() {
		return email_pessoal;
	}
	public void setEmail_pessoal(String email_pessoal) {
		this.email_pessoal = email_pessoal;
	}
	public String getEmail_particular() {
		return email_particular;
	}
	public void setEmail_particular(String email_particular) {
		this.email_particular = email_particular;
	}
	public String getDepartamento() {
		return departamento;
	}
	public void setDepartamento(String departamento) {
		this.departamento = departamento;
	}	
}